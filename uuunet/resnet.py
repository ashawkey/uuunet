class PAM(nn.Module):
    """ Position attention module"""
    def __init__(self, in_dim):
        super().__init__()
        self.chanel_in = in_dim

        self.query_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim//8, kernel_size=1)
        self.key_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim//8, kernel_size=1)
        self.value_conv = nn.Conv2d(in_channels=in_dim, out_channels=in_dim, kernel_size=1)
        self.gamma = nn.Parameter(torch.zeros(1))
        self.softmax = nn.Softmax(dim=-1)

    def forward(self, x):
        """
            inputs :
                x : input feature maps( B X C X H X W)
            returns :
                out : attention value + input feature
                attention: B X (HxW) X (HxW)
        """
        m_batchsize, C, height, width = x.size()
        proj_query = self.query_conv(x).view(m_batchsize, -1, width*height).permute(0, 2, 1)
        proj_key = self.key_conv(x).view(m_batchsize, -1, width*height)
        energy = torch.bmm(proj_query, proj_key)
        attention = self.softmax(energy)
        proj_value = self.value_conv(x).view(m_batchsize, -1, width*height)

        out = torch.bmm(proj_value, attention.permute(0, 2, 1))
        out = out.view(m_batchsize, C, height, width)

        out = self.gamma*out + x
        return out

class CAM(nn.Module):
    """ Channel attention module"""
    def __init__(self, in_dim):
        super().__init__()
        self.chanel_in = in_dim

        self.gamma = nn.Parameter(torch.zeros(1))
        self.softmax = nn.Softmax(dim=-1)

    def forward(self,x):
        """
            inputs :
                x : input feature maps( B X C X H X W)
            returns :
                out : attention value + input feature
                attention: B X C X C
        """
        m_batchsize, C, height, width = x.size()
        proj_query = x.view(m_batchsize, C, -1)
        proj_key = x.view(m_batchsize, C, -1).permute(0, 2, 1)
        energy = torch.bmm(proj_query, proj_key)
        energy_new = torch.max(energy, -1, keepdim=True)[0].expand_as(energy)-energy
        attention = self.softmax(energy_new)
        proj_value = x.view(m_batchsize, C, -1)

        out = torch.bmm(attention, proj_value)
        out = out.view(m_batchsize, C, height, width)

        out = self.gamma*out + x
        return out

class ConvBR2d(nn.Module):
    def __init__(self, in_channels, out_channels, kernel_size=1, stride=1, padding=0, dilation=1, groups=1, is_activation=True):
        super().__init__()
        self.conv = nn.Conv2d(in_channels, out_channels, kernel_size=kernel_size, padding=padding, dilation=dilation, stride=stride, groups=groups, bias=False)
        self.bn = nn.BatchNorm2d(out_channels)
        self.is_activation = is_activation
        
        if is_activation:
            self.relu = nn.ReLU(inplace=True)
        
    def forward(self, x):
        x = self.bn(self.conv(x))
        if self.is_activation:
            x = self.relu(x)
        return x


class DANet(SegmentationNetwork):
    def __init__(self, norm_layer=nn.BatchNorm2d, **kwargs):
        super().__init__()

        self.conv_op = nn.Conv2d
        self.input_shape_must_be_divisible_by = [8, 8]
        
        self.pretrained0 = resnet50(pretrained=True,
                                           dilated=True,
                                           norm_layer=norm_layer,
                                           multi_grid=False,
                                           multi_dilation=None)

        self.pretrained1 = resnet50(pretrained=True, 
                                           dilated=True,
                                           norm_layer=norm_layer,
                                           multi_grid=False,
                                           multi_dilation=None)

        self.decoder = nn.Sequential(
                ConvBR2d(512, 128, 3, 1, 1),
                ConvBR2d(128, 128, 3, 1, 1),
            )

        self.head0 = nn.Sequential(nn.Dropout2d(0.1, False), nn.Conv2d(128, 5, 1))
        self.head1 = nn.Sequential(nn.Dropout2d(0.1, False), nn.Conv2d(128, 2, 1))

    def forward(self, x, modality):
        imsize = x.size()[2:]

        if modality == 0:
            x = self.pretrained0.conv1(x)
            x = self.pretrained0.bn1(x)
            x = self.pretrained0.relu(x)
            x = self.pretrained0.maxpool(x)
            c1 = self.pretrained0.layer1(x)
            c2 = self.pretrained0.layer2(c1)
            c3 = self.pretrained0.layer3(c2)
            c4 = self.pretrained0.layer4(c3)
            x = self.decoder(c4)
            x = self.head0(x)

        if modality == 1:
            x = self.pretrained1.conv1(x)
            x = self.pretrained1.bn1(x)
            x = self.pretrained1.relu(x)
            x = self.pretrained1.maxpool(x)
            c1 = self.pretrained1.layer1(x)
            c2 = self.pretrained1.layer2(c1)
            c3 = self.pretrained1.layer3(c2)
            c4 = self.pretrained1.layer4(c3)
            x = self.decoder(c4)
            x = self.head1(x)

        x = F.interpolate(x, size=imsize, mode='bilinear')

        return x
        
class DANetHead(nn.Module):
    def __init__(self, in_channels, norm_layer):
        super(DANetHead, self).__init__()
        inter_channels = in_channels // 4
        self.conv5a = nn.Sequential(nn.Conv2d(in_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())
        
        self.conv5c = nn.Sequential(nn.Conv2d(in_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())

        self.sa = PAM(inter_channels)
        self.sc = CAM(inter_channels)
        self.conv51 = nn.Sequential(nn.Conv2d(inter_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())
        self.conv52 = nn.Sequential(nn.Conv2d(inter_channels, inter_channels, 3, padding=1, bias=False),
                                   norm_layer(inter_channels),
                                   nn.ReLU())

    def forward(self, x):
        feat1 = self.conv5a(x)
        sa_feat = self.sa(feat1)
        sa_conv = self.conv51(sa_feat)

        feat2 = self.conv5c(x)
        sc_feat = self.sc(feat2)
        sc_conv = self.conv52(sc_feat)

        feat_sum = sa_conv+sc_conv
        
        return feat_sum

